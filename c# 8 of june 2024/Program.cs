//increment a number by 1
num++;
Console.WriteLine("num is "+ num);
//increment a number by 15
num += 15;
Console.WriteLine($"num is {num}");
//decrement the num by 5
num -= 5;
Console.WriteLine($"num is {num}");
//decrement a number by 1
num--;
Console.WriteLine($"num is {num}");
//multiply the number by 12
num *= 12;
Console.WriteLine($"num is {num}");
//divide the number by 6
num /= 6;
Console.WriteLine($"num is {num}"  );
    
